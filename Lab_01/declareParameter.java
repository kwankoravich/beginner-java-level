package declareParameter;
public class declareParameter {
	public static void main(String[] args) {
		// This is the first function to declare the parameters
		int num = 0;
		boolean boo = true;
		String text = "Test";
		double num_1 = 1.02;
		char character = 'a';
		/* Print the parameter that we mention above
		 * num,boo,text,num_1,character
		 */
		System.out.println("This is a String " + num);
		System.out.println("This is a String " + boo);
		System.out.println("This is a String " + text);
		System.out.println("This is a String " + num_1);
		System.out.println("This is a String " + character);
		
	}

}
